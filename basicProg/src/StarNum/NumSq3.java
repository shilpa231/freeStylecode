package StarNum;

public class NumSq3 {

	public static void main(String[] args) {

		char c = 'A';
		for (int i = 1; i <= 5; i++) {
			for (int j = 1; j <= 5; j++) {
				
				System.out.print(c+ " ");
			
			}
			c++;
			System.out.println(" ");
		}
	}

}
