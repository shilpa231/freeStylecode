package StarNum;

public class NumSq4 {

	public static void main(String[] args) {
		//char c = 'A';
		for (int i = 1; i <= 5; i++) {
			char c = 'A';
			for (int j = 1; j <= 5; j++) {
				System.out.print(c + " ");
				c++;
			}
			System.out.println(" ");
		}
	}

}

